	function vefificaEspacoCaronas() {
		if($(window).height() <= 970) {
			$('.desaparecer-1024').hide();
		} else {
			$('.desaparecer-1024').show();
		}

		if($(window).height() <= 830) {
			$('.desaparecer-860').hide();
		} else {
			$('.desaparecer-860').show();
		}
	}

	$('form [type=checkbox]').on('change', function() {
		$(this).parents('label').find('.ico-lembrar-senha').toggleClass('ico-lembrar-senha-checked');
	});

	//ABRE RECUPERA SENHA // TEMOS DE USO
	$('.esqueci-senha , .termos-uso').on('click', function(event) {

		event.preventDefault();

		$('.light-boxes').fadeIn(function() {

			$('.recuperar-senha , #termos').stop().animate({
				opacity: 1,
				left : 225
			});
		});
	});

	//FECHA RECUPERASENHA // TEMOS DE USO
	$('.fechar-boxes.recupera , .light-boxes').on('click', function() {

		$('.recuperar-senha , #termos').stop().animate({
			opacity: 0,
			left : -240
		}, function() {
			$('.light-boxes').fadeOut();
		});
	});


	//SELECIONAR SEXO
	$('.ico-sexo').on('click', function(event) {

		event.preventDefault();

		var 
			tipoSexo = $(this).attr('data-sexo');

		$('.ico-sexo').removeClass('active');
		$(this).toggleClass('active').parents('.lado-cadastro-b').find('[name="tiposexo"]').val(tipoSexo);
	});

	//TERMOS DE USO
	$('.scroll-lindo').jScrollPane();

	vefificaEspacoCaronas();

